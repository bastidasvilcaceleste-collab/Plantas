def filtrar(cultivo=None, patogeno=None, busqueda=None):
    resultados = ENFERMEDADES
    if cultivo:
        resultados = [e for e in resultados if e['cultivo'] == cultivo]
    if patogeno:
        resultados = [e for e in resultados if e['patogeno'] == patogeno]
    if busqueda:
        busqueda = busqueda.lower()
        resultados = [e for e in resultados if
                      busqueda in e['nombre'].lower()
                      or busqueda in e['cultivo'].lower()
                      or busqueda in e['sintomas'].lower()]
    vistos = set()
    unicos = []
    for e in resultados:
        if e['clave'] not in vistos:
            vistos.add(e['clave'])
            unicos.append(e)
    return unicos


def buscar_por_clave(clave):
    for e in ENFERMEDADES:
        if e['clave'] == clave:
            return e
    return None
